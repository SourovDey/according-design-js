
function hiddenMe(){
    let hiddenText = document.querySelector(".item__content");
    let downArrow = document.querySelector(".down-arrow")


    hiddenText.classList.toggle("hide-text")
    downArrow.classList.toggle("rotate-arrow")
    
}


