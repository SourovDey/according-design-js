
function hiddenMe(){
    let hiddenText = document.querySelector(".item__content1");
    let upArrow = document.querySelector(".up-arrow1")
    let downArrow = document.querySelector(".down-arrow1")


    hiddenText.classList.add("hide-text")
    upArrow.classList.add("hide-arrow")
    downArrow.classList.remove("hide-arrow")
}
function showMe(){
    let hiddenText = document.querySelector(".item__content1");
    let upArrow = document.querySelector(".up-arrow1")
    let downArrow = document.querySelector(".down-arrow1")


    hiddenText.classList.remove("hide-text")
    upArrow.classList.remove("hide-arrow")
    downArrow.classList.add("hide-arrow")
    
}





